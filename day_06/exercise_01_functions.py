# Defining and calling functions

# Built in function
print('Hello')
num_char = len('Hello')
print(num_char)

# Define function with def keyword followed by a parantheses and line ending colon

def my_function():
    print('Hello')
    print('Bye')

# Call a function by calling the function name followed by parentheses - where any params might go
my_function()

cool_number = 12 + 30
cool_number * 5  # Multiplying a variable does not affect it's assigned value
print(cool_number)
